    public void generalLaberinto() {
        int filas = Integer.parseInt((String) cbxfilas.getSelectedItem());
        int columnas = Integer.parseInt((String) cdxcolum.getSelectedItem());
        cont.setlaberinto(filas, columnas);
        cont.generalLaberinto();
        actualizarTabla();
    }

    public void numeroCombo() {
        cbxfilas.removeAllItems();
        cdxcolum.removeAllItems();
        for (int i = 10; i <= 100; i++) {
            cbxfilas.addItem(String.valueOf(i));
            cdxcolum.addItem(String.valueOf(i));
        }
    }

    public void actualizarTabla() {
        ModeloTablaLaberinto lab = new ModeloTablaLaberinto();
        lab.setLaberinto(cont.getLaberinto());
        Laberinto.setModel(lab);
    }

